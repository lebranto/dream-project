    CONSTRAINT PK_REVIEW PRIMARY KEY ("REVIEW_ID"),
    CONSTRAINT CHK_REVIEW_RATING      CHECK ("REVIEW_RATING" BETWEEN 1 AND 5),
    CONSTRAINT FK_REVIEW_MEMBER       FOREIGN KEY ("MEMBER_NO") REFERENCES "MEMBER"       ("MEMBER_NO"),
    CONSTRAINT FK_REVIEW_ORDER_DETAIL FOREIGN KEY ("DETAIL_ID") REFERENCES "ORDER_DETAIL" ("DETAIL_ID")
