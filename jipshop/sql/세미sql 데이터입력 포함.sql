    ----------------------------------------------------------------
    -- 회원 20명 중 랜덤 1명
    ----------------------------------------------------------------
    PROCEDURE get_random_member(p_member_no OUT NUMBER) IS
    BEGIN
        SELECT member_no
          INTO p_member_no
          FROM (
                SELECT member_no
                  FROM (
                        SELECT member_no
                          FROM MEMBER
                         ORDER BY member_no
                       )
                 WHERE ROWNUM <= 20
                 ORDER BY DBMS_RANDOM.VALUE
               )
         WHERE ROWNUM = 1;
    END;

    ----------------------------------------------------------------
    -- 활성 상품 랜덤 1개
    ----------------------------------------------------------------
    PROCEDURE get_random_product(
        p_product_id OUT NUMBER,
        p_price      OUT NUMBER
    ) IS
    BEGIN
        SELECT product_id, product_price
          INTO p_product_id, p_price
          FROM (
                SELECT product_id, product_price
                  FROM PRODUCT
                 WHERE PRODUCT_ACTIVE_YN = 'Y'
                 ORDER BY DBMS_RANDOM.VALUE
               )
         WHERE ROWNUM = 1;
    END;

    ----------------------------------------------------------------
    -- 활성 상품 랜덤 1개 (특정 상품 제외)
    ----------------------------------------------------------------
    PROCEDURE get_random_product_exclude(
        p_exclude_id IN NUMBER,
        p_product_id OUT NUMBER,
        p_price      OUT NUMBER
    ) IS
    BEGIN
        SELECT product_id, product_price
          INTO p_product_id, p_price
          FROM (
                SELECT product_id, product_price
                  FROM PRODUCT
                 WHERE PRODUCT_ACTIVE_YN = 'Y'
                   AND PRODUCT_ID <> p_exclude_id
                 ORDER BY DBMS_RANDOM.VALUE
               )
         WHERE ROWNUM = 1;
    END;

    ----------------------------------------------------------------
    -- 취소 사유 랜덤
    ----------------------------------------------------------------
    FUNCTION get_reason(p_idx NUMBER) RETURN VARCHAR2 IS
    BEGIN
        RETURN CASE MOD(p_idx, 5)
                 WHEN 0 THEN '단순 변심'
                 WHEN 1 THEN '주문 실수'
                 WHEN 2 THEN '상품 불량'
                 WHEN 3 THEN '오배송'
                 ELSE '배송 지연'
               END;
    END;

    ----------------------------------------------------------------
