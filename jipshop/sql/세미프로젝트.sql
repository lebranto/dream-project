    "EXPECTED_DATE" DATE         NULL,
    CONSTRAINT PK_DELIVERY PRIMARY KEY ("DELIVERY_ID"),
    CONSTRAINT FK_DELIVERY_ORDERS FOREIGN KEY ("ORDER_ID") REFERENCES "ORDERS" ("ORDER_ID")
);

CREATE TABLE "STATUS" (
    "DNUM"            VARCHAR2(50) NOT NULL,
    "DELIVERY_YN"     CHAR(1)      NULL,
    "DELIVERY_STATUS" VARCHAR2(20) NOT NULL,
    "DELIVERY_ID"     NUMBER       NOT NULL,
    CONSTRAINT PK_STATUS PRIMARY KEY ("DNUM"),
    CONSTRAINT FK_STATUS_DELIVERY FOREIGN KEY ("DELIVERY_ID") REFERENCES "DELIVERY" ("DELIVERY_ID")
);

COMMENT ON COLUMN "STATUS"."DELIVERY_STATUS" IS '배송중/배송완료 둘 중 하나의 값. 배송완료가 Y로 되어 있으면 배송 완료로 값을 변경';

-- [수정] REVIEW_RATING CHECK 제약 추가 (1~5 별점)
CREATE TABLE "REVIEW" (
    "REVIEW_ID"        NUMBER         NOT NULL,
    "MEMBER_NO"        NUMBER         NOT NULL,
    "DETAIL_ID"        NUMBER         NOT NULL,
    "REVIEW_RATING"    NUMBER(1)      NOT NULL,
    "REVIEW_CONTENT"   VARCHAR2(1000) NOT NULL,
