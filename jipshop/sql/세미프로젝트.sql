CREATE TABLE "ORDERS" (
    "ORDER_ID"            NUMBER        NOT NULL,
    "MEMBER_NO"           NUMBER        NOT NULL,
    "ORDER_DATE"          DATE DEFAULT SYSDATE NOT NULL,
    "ORDER_TOTAL_PRICE"   NUMBER        NOT NULL,
    "ORDER_DELIVERY_FEE"  NUMBER        NOT NULL,
    "ORDERER_NAME"        VARCHAR2(50)  NOT NULL,
    "ORDERER_PHONE"       VARCHAR2(20)  NOT NULL,
    "ORDERER_EMAIL"       VARCHAR2(100) NULL,
    "RECV_NAME"           VARCHAR2(50)  NOT NULL,
    "RECV_ADDRESS"        VARCHAR2(200) NOT NULL,
    "RECV_PHONE"          VARCHAR2(20)  NOT NULL,
    "ORDER_MEMO"          VARCHAR2(300) NULL,
    "ORDER_CANCEL_YN"     CHAR(1) DEFAULT 'N' NOT NULL,
    "CANCEL_REQUEST_DATE" DATE          NULL,
    "CANCEL_REASON"       VARCHAR2(200) NULL,
    "CANCEL_STATUS"       VARCHAR2(10)  NULL,
    CONSTRAINT PK_ORDERS PRIMARY KEY ("ORDER_ID"),
    CONSTRAINT FK_ORDERS_MEMBER FOREIGN KEY ("MEMBER_NO") REFERENCES "MEMBER" ("MEMBER_NO")
