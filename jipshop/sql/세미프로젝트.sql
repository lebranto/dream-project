CREATE TABLE "MEMBER" (
    "MEMBER_NO"         NUMBER          NOT NULL,
    "MEMBER_ID"         VARCHAR2(50)    NOT NULL,
    "MEMBER_PWD"         VARCHAR2(255)   NOT NULL,
    "MEMBER_NAME"       VARCHAR2(50)    NOT NULL,
    "MEMBER_ADDRESS"    VARCHAR2(200)   NOT NULL,
    "PHONE"             VARCHAR2(20)    NOT NULL,
    "EMAIL"             VARCHAR2(100)   NULL,
    "MEMBER_JOIN_DATE"  DATE DEFAULT SYSDATE NOT NULL,
    "MEMBER_ACTIVE_YN"  CHAR(1) DEFAULT 'Y' NOT NULL,
    CONSTRAINT PK_MEMBER PRIMARY KEY ("MEMBER_NO")
);

CREATE TABLE "AUTHORITIES" (
    "MEMBER_NO"  NUMBER       NOT NULL,
    "AUTHORITY"  VARCHAR2(15) NULL,
    CONSTRAINT PK_AUTHORITIES PRIMARY KEY ("MEMBER_NO"),
    CONSTRAINT FK_AUTHORITIES_MEMBER FOREIGN KEY ("MEMBER_NO") REFERENCES "MEMBER" ("MEMBER_NO")
);

CREATE TABLE "PERSISTENT_LOGINS" (
    "TOKEN"     VARCHAR2(64) NULL,
    "SERIES"    VARCHAR2(64) NULL,
    "LAST_USED" TIMESTAMP(6) NULL,
    "USERNAME"  VARCHAR2(64) NULL
);

-- [수정] CATEGORY_PARENT_ID: NOT NULL -> NULL (NULL=최상위 카테고리)
CREATE TABLE "PRODUCT_CATEGORY" (
    "CATEGORY_ID"        NUMBER       NOT NULL,
    "CATEGORY_NAME"      VARCHAR2(50) NOT NULL,
    "CATEGORY_PARENT_ID" NUMBER       NULL,
    CONSTRAINT PK_PRODUCT_CATEGORY PRIMARY KEY ("CATEGORY_ID")
);

COMMENT ON COLUMN "PRODUCT_CATEGORY"."CATEGORY_PARENT_ID" IS '상위 카테고리 ID (자기참조, NULL=최상위)';

ALTER TABLE "PRODUCT_CATEGORY" ADD CONSTRAINT FK_PRODUCT_CATEGORY_SELF
    FOREIGN KEY ("CATEGORY_PARENT_ID") REFERENCES "PRODUCT_CATEGORY" ("CATEGORY_ID");

CREATE TABLE "COMPANY" (
    "COMPANY_CODE"    NUMBER        NOT NULL,
    "COMPANY_NAME"    VARCHAR2(100) NULL,
    "COMPANY_PHONE"   VARCHAR2(20)  NULL,
    "ADDRESS_ADDRESS" VARCHAR2(200) NULL,
    CONSTRAINT PK_COMPANY PRIMARY KEY ("COMPANY_CODE")
);

CREATE TABLE "PRODUCT" (
    "PRODUCT_ID"        NUMBER        NOT NULL,
    "CATEGORY_ID"       NUMBER        NOT NULL,
    "COMPANY_CODE"      NUMBER        NOT NULL,
    "PRODUCT_NAME"      VARCHAR2(200) NOT NULL,
    "PRODUCT_PRICE"     NUMBER        NOT NULL,
    "PRODUCT_STOCK"     NUMBER DEFAULT 0 NOT NULL,
    "PRODUCT_DETAIL"    CLOB          NOT NULL,
    "PRODUCT_PHOTO1"    VARCHAR2(300) NOT NULL,
    "PRODUCT_PHOTO2"    VARCHAR2(300) NULL,
    "PRODUCT_REG_DATE"  DATE DEFAULT SYSDATE NOT NULL,
    "PRODUCT_ACTIVE_YN" CHAR(1) DEFAULT 'Y' NOT NULL,
    "PET_TYPE"          VARCHAR2(10)  NULL,
    "AGE_GROUP"         VARCHAR2(10)  NULL,
    CONSTRAINT PK_PRODUCT PRIMARY KEY ("PRODUCT_ID"),
    CONSTRAINT FK_PRODUCT_CATEGORY FOREIGN KEY ("CATEGORY_ID")  REFERENCES "PRODUCT_CATEGORY" ("CATEGORY_ID"),
    CONSTRAINT FK_PRODUCT_COMPANY  FOREIGN KEY ("COMPANY_CODE") REFERENCES "COMPANY"          ("COMPANY_CODE")
);

COMMENT ON COLUMN "PRODUCT"."PRODUCT_ACTIVE_YN" IS '품절되면 N으로 바뀌게';

CREATE TABLE "PET" (
    "PET_NO"     NUMBER        NOT NULL,
    "MEMBER_NO"  NUMBER        NOT NULL,
    "PET_TYPE"   VARCHAR2(20)  NOT NULL,
    "PET_NAME"   VARCHAR2(50)  NOT NULL,
    "PET_AGE"    DATE          NULL,
    "PET_WEIGHT" NUMBER(5,2)   NULL,
    "PET_PHOTO"  VARCHAR2(300) NULL,
    CONSTRAINT PK_PET PRIMARY KEY ("PET_NO"),
    CONSTRAINT FK_PET_MEMBER FOREIGN KEY ("MEMBER_NO") REFERENCES "MEMBER" ("MEMBER_NO")
);

COMMENT ON COLUMN "PET"."PET_PHOTO" IS '사진 파일 경로 (서버 상대경로)';

CREATE TABLE "CART" (
    "CART_ID"       NUMBER NOT NULL,
    "MEMBER_NO"     NUMBER NOT NULL,
    "PRODUCT_ID"    NUMBER NOT NULL,
    "CART_QTY"      NUMBER NOT NULL,
    "CART_REG_DATE" DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_CART PRIMARY KEY ("CART_ID"),
    CONSTRAINT FK_CART_MEMBER  FOREIGN KEY ("MEMBER_NO")  REFERENCES "MEMBER"  ("MEMBER_NO"),
    CONSTRAINT FK_CART_PRODUCT FOREIGN KEY ("PRODUCT_ID") REFERENCES "PRODUCT" ("PRODUCT_ID")
);

CREATE TABLE "WISHLIST" (
    "WISH_ID"    NUMBER NOT NULL,
    "MEMBER_NO"  NUMBER NOT NULL,
